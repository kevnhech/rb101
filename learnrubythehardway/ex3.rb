puts "Is it true that 3 + 2 < 5 - 7?" 

puts 3.0 + 2 < 5 - 7 # prints boolean "false"

puts "What is 3 + 2? #{3.0 + 2}" # prints "What is 3 + 2? 5.0"
puts "What is 5 - 7? #{5.0 - 7}" # prints "What is 5 - 7? -2.0"

puts "Oh, that's why it's false." # prints whatever is in the quotes

puts "How about some more." # prints whatever is in the quotes

puts "Is it greater? #{5.0 > -2}" # prints "Is it greater? true"
puts "Is it greater or equal? #{5.0 >= -2}" # prints "Is it greater or equal? true"
puts "Is it less or equal? #{5.0 <= -2}" # prints "Is it less or equal? false"