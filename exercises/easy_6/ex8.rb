# Given an unordered array and the information that exactly one value in the array occurs twice (every other value occurs exactly once), how would you determine which value occurs twice? Write a method that will find and return the duplicate value that is known to be in the array.

# initialize array `arr`
# sort the original array from least to greatest
# iterate through the array
#   append the element of the array to `arr` if it is equal to the next element
# end the block
# return the first integer of `arr`

def find_dup(array)
  array.sort!
  dup = 0
  idx = 0
  loop do
    break if idx == array.length
    dup += array[idx] if array[idx] == array[idx + 1]
    idx += 1
  end
  dup
end

